//definir varables 
let numero_uno, numero_dos,cociente, residuo;

numero_uno = Number(prompt('Ingrese el primero Numero'));
numero_dos = Number(prompt('Ingrese el segundo Numero'));

//Proceso
cociente = numero_uno / numero_dos;
residuo = numero_uno % numero_dos;

document.write('<h1>El cociente es: ' + cociente + ' y el residuo es: ' + residuo + '<h1>');

